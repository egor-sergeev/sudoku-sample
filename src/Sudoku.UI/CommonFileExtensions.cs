namespace Sudoku.UI;

/// <summary>
/// Defines the constants that are the common file extensions.
/// </summary>
internal static class CommonFileExtensions
{
	/// <summary>
	/// Indicates all file extensions are accepted.
	/// </summary>
	public const string All = "*";

	/// <summary>
	/// Indicates the plain-text format.
	/// </summary>
	public const string Text = ".txt";

	/// <summary>
	/// Indicates the sudoku file.
	/// </summary>
	public const string Sudoku = ".sdk";

	/// <summary>
	/// Indicates the drawing file.
	/// </summary>
	public const string SudokuWithDrawing = ".sdd";

	/// <summary>
	/// Indicates the configuration file.
	/// </summary>
	public const string Configuration = ".sdc";
}
